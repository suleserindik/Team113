package day11_stringManipulations_ForLoop;

public class C08_Loops {

    public static void main(String[] args) {
        // 10 kere "Java candir" yazdirin

        System.out.println("Java candir");
        // 10 kez yazdirmak yerine Loops kullanacagiz

        for (int i = 1; i <10 ; i++) {
            System.out.println(i +"-Java candir"); // 10 kez sayarak yazdirir
        }






    }
}
